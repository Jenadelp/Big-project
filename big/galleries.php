<h3>What is jQuery?</h3>

 
<p>“jQuery is an opensource library that is free and supported well across different jQuery is an efficient & fast JavaScript Library created by John Resig in 2006. The motto of jQuery is write less, do more, which is very apt because it’s functionality revolves around simplifying each and every line of code. (Lateef)” </p>
 
<p>A jQuery slider is a carousel used to display information in a rotation. The main components are a container box with a slide for the content: images, videos, buttons, text etc. There is also a navigation tool or scroll for guiding the user through the slides.</p>
 
<p>jQuery sliders have a variety of effects to create a smooth transition when cycling through information. There are many ways to create a jQuery slider.  You can use Bootstrap,  jQuery plugins, or build one from scratch.</p> 
 
<h3>jQuery Pros</h3>
 
<p>jQuery sliders can be useful for delivering a large amount of information that is easily absorbed by the reader while utilizing a small amount of space. Readers can forgo scrolling through large amounts of text to find the information they are searching for. Sliders appeal to the visual learner and capture an audience through the appeal of design.</p>
 
<p>Sliders can be useful for displaying an album of photographs, telling a story, or for marketing, like when you want to display a series of photos for a product with the goal of presenting the product from a variety of angles. The slider can hold the buyer’s interest by continuing to engage them with the product.</p>
 
<h3>jQuery Cons</h3>
 
<p>Some sliders use high quality and large images with JavaScript files that can slow down your web page. A poorly made slider can ruin your SEO score or search engine optimization. It can slow down a website which is negative for SEO as well as user experience.</p> 
 
<p>Few slider plugins follow responsive design practices, which means sliders may not work correctly on small screens. A majority of web traffic comes from mobile devices, so it’s vital a site function properly for its mobile users.</p>
 
<p>“Sliders allegedly trigger banner blindness. According to NN Group, banner blindness refers to people’s tendency to ignore page elements they consider to be ads. However, it’s your choice of what to include in the website slider that makes a major difference. A slider with meaningful content, a carefully crafted message and optimal typography won’t fall into this banner blindness trap.(Raducan)”</p>



